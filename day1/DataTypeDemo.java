class DataTypeDemo {
	
	public static void main(String[]  args) {
		
		//Integer data types

		//Size 1 byte memory = 8 bits
		byte age = 12;		
		System.out.println(age);

		//Size 2 bytes memory = 16 bits
		short pages = 720;		
		System.out.println(pages);

		//Size 4 bytes memory = 32 bits
		int count = 6512800;		
		System.out.println(count);

		//Size 8 bytes memory = 64 bits
		long amount = 98000000000L;		
		System.out.println(amount);


		//Decimal data types
		
		//Size 4 bytes memory = 32 bits
		float random = 8120.52622F;		
		System.out.println(random);

		//Size 8 bytes memory = 64 bits
		double salary = 700000000.00;		
		System.out.println(salary);

		//Character data types
		
		//Size 2 bytes memory = 16 bits
		char letter = 'R';		
		System.out.println(letter);


		//Boolean data types
		
		//Size 1 bit memory
		boolean passed = true;		
		System.out.println(passed);

	}
}
